


class Calculator:

    def add(self, a, b):
        return a + b

    def subtract(self, a, b):
        return a - b

    def multiply(self, a, b):
        return b * a

    def divide(self, a, b):
        return a / b

    def reverse_subtract(self, a, b):
        return b - a

    def reverse_divide(self, a, b):
        return b / a

